public class FlickrFeeds {

    private String strFeedTitle;
    private String strFeedLink;
    private String strFeedImageURL;
    private String strFeedAuthor;
    private String strFeedTag;


    public String getStrFeedTitle() {
        return strFeedTitle;
    }

    public void setStrFeedTitle(String strFeedTitle) {
        this.strFeedTitle = strFeedTitle;
    }

    public String getStrFeedLink() {
        return strFeedLink;
    }

    public void setStrFeedLink(String strFeedLink) {
        this.strFeedLink = strFeedLink;
    }

    public String getStrFeedImageURL() {
        return strFeedImageURL;
    }

    public void setStrFeedImageURL(String strFeedImageURL) {
        this.strFeedImageURL = strFeedImageURL;
    }

    public String getStrFeedAuthor() {
        return strFeedAuthor;
    }

    public void setStrFeedAuthor(String strFeedAuthor) {
        this.strFeedAuthor = strFeedAuthor;
    }

    public String getStrFeedTag() {
        return strFeedTag;
    }

    public void setStrFeedTag(String strFeedTag) {
        this.strFeedTag = strFeedTag;
    }
}
